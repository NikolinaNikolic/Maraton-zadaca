
public class Maraton {
	
	   String ime;
	     
	    int vrijeme;
	     
	    public Maraton(String ime, int vrijeme) 
	    {
	        this.ime = ime;
	         
	        this.vrijeme = vrijeme;
	    }
	    public void setName(String name) {
	    	this.ime = name;
	    	}

	    	public void setTime(int time) {
	    	this.vrijeme = time;
	    	}

	    	public String getName() {
	    	return ime;
	    	}

	    	public int getTime() {
	    	return vrijeme;
	    	}

	    	}

	





